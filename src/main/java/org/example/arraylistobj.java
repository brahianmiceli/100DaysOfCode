package org.example;

public class arraylistobj {
    private String foodFirst;
    private String waterFirst;


    public arraylistobj(String foodFirst, String waterFirst) {
        this.foodFirst = foodFirst;
        this.waterFirst = waterFirst;
    }

    public String getFoodFirst() {
        return foodFirst;
    }

    public void setFoodFirst(String foodFirst) {
        this.foodFirst = foodFirst;
    }

    public String getWaterFirst() {
        return waterFirst;
    }

    public void setWaterFirst(String waterFirst) {
        this.waterFirst = waterFirst;
    }




}
